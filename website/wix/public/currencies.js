const displayNameFactory = () => {
  if ('DisplayNames' in Intl) {
    const displayNames = new Intl.DisplayNames('en', { type: 'currency' });
    return (code) => displayNames.of(code.toUpperCase());
  } else return (code) => code.toUpperCase();
};

export const currencyOptions = () => {
  const displayNameOf = displayNameFactory();
  return currencies.map((code) => ({
    value: code,
    label: displayNameOf(code),
  }));
};
